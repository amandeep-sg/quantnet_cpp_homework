// c program to check whether the person is married or not
// 1 means married and 0 means not married
//
// author: amandeep singh gujral
#include <stdio.h> // c style I/O

int main()
{
    int married = 0;                                                                 // married state
    (married) ? printf("Person is married.\n") : printf("Person is not married.\n"); // conditional or ternary operator

    return 0;
}